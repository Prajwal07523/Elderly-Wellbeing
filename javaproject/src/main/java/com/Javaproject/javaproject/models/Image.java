package com.Javaproject.javaproject.models;

public class Image {
    private String url;
    private String alt;

    // Getters and setters

    public String getUrl() {
        return url;
    }

    public String getAlt() {
        return alt;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public void setAlt(String alt) {
        this.alt = alt;
    }
}
